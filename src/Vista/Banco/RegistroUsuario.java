    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        UsuarioDB usuarioDB = new UsuarioDB();
        RegistroUsuarioSatisfactorio rus = new RegistroUsuarioSatisfactorio();
        String nombre, apellido, dpi, telefono;
        char genero;
        nombre = txtNombre.getText();
        apellido = txtApellido.getText();
        dpi = txtDPI.getText();
        telefono = txtTelefono.getText();
        int tipoCuenta = cBoxtipoCuenta.getSelectedIndex();

        if (cBoxGenero.getSelectedIndex() == 0) {
            genero = 'H';
        } else {
            genero = 'M';
        }

        if (txtNombre.getText().isEmpty() || txtApellido.getText().isEmpty() || txtDPI.getText().isEmpty() || txtTelefono.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Porfavor rellene todos los campos", "Error", JOptionPane.ERROR_MESSAGE);

        } else {

            try {
                if (usuarioDB.existeUsuario(dpi) == true) {
                    JOptionPane.showMessageDialog(null, "Esta persona ya est√° registrada", "Error", JOptionPane.ERROR_MESSAGE);
                } else {
                    Usuario usuario = new Usuario(nombre, apellido, dpi, telefono, genero);

                    switch (tipoCuenta) {

                        case 0:
                            CuentaAhorro cuentaA = new CuentaAhorro(usuario);
                            try {
                                usuarioDB.insertarCuentaA(cuentaA);
                                rus.setInfoA(cuentaA);
                                rus.setVisible(true);
                                this.setVisible(false);
                                JOptionPane.showMessageDialog(null, "Usuario registrado correctamente", "Registro de Usuario", JOptionPane.INFORMATION_MESSAGE);

                            } catch (SQLException e) {
                                e.printStackTrace();
                                e.getMessage();
                                JOptionPane.showMessageDialog(null, "No se puedo registrar el usuario", "Registro de Usuario", JOptionPane.WARNING_MESSAGE);
                            } finally {
                                txtNombre.setText("");
                                txtApellido.setText("");
                                txtDPI.setText("");
                                txtTelefono.setText("");
                            }
                            break;

                        case 1:
                            CuentaMonetaria cuentaM = new CuentaMonetaria(usuario);
                            try {
                                usuarioDB.insertarCuentaM(cuentaM);
                                rus.setInfoA(cuentaM);
                                rus.setVisible(true);
                                this.setVisible(false);
                                JOptionPane.showMessageDialog(null, "Usuario registrado correctamente", "Registro de Usuario", JOptionPane.INFORMATION_MESSAGE);
                            } catch (SQLException e) {
                                e.printStackTrace();
                                e.getMessage();
                                JOptionPane.showMessageDialog(null, "No se puedo registrar el usuario", "Registro de Usuario", JOptionPane.WARNING_MESSAGE);
                            } finally {
                                txtNombre.setText("");
                                txtApellido.setText("");
                                txtDPI.setText("");
                                txtTelefono.setText("");
                            }
                            break;

                    }

                }
            } catch (SQLException ex) {
                Logger.getLogger(RegistroUsuario.class.getName()).log(Level.SEVERE, null, ex);
            }

        }


    }//GEN-LAST:event_btnRegistrarActionPerformed

