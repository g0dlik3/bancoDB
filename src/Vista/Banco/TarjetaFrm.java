    private void btnSolicitarTarjetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSolicitarTarjetaActionPerformed
        String numCuenta = null;
        Tarjeta tarjeta = new Tarjeta();
        Cuenta cuenta = new CuentaAhorro();
        

        int columnSelect = 1;
        int rowSelect = jTabla.getSelectedRow();
        //System.out.println(columnSelect + " " + rowSelect);

        try {
            numCuenta = (String) jTabla.getValueAt(rowSelect, columnSelect);
            cuenta.setNumeroCuenta(numCuenta);
            tarjeta.setCuenta(cuenta);
            //System.out.println(numTarjeta);
        } catch (ArrayIndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(null, "No ha seleccionado una cuenta, seleccione una para poder continuar", "Error", JOptionPane.ERROR_MESSAGE);
        }

        if (numCuenta != null) {
            TarjetaDB tarjetadb = new TarjetaDB();
            System.out.println(cuenta.toString());
            System.out.println(tarjeta.toString());
            try {
                if(tarjetadb.insertar(tarjeta) == true){
                    Utilidades utilidad = new Utilidades();
                    
                    String numTarjeta = utilidad.getSpaceNumTarjeta(tarjeta.getNumeroTarjeta());
                    DatosTarjeta mitarjeta = new DatosTarjeta();
                    mitarjeta.lblNumTarjeta.setText(numTarjeta);
                    mitarjeta.lblTitular.setText(lblNombre.getText());
                    mitarjeta.lblFechaCreacion.setText(tarjeta.getFechaCreacion());
                    mitarjeta.lblFechaVenc.setText(tarjeta.getFechaExpiracion());
                    
                    mitarjeta.setVisible(true);
                    
                    JOptionPane.showConfirmDialog(null, "Tarjeta creada exitosamente", "Tarjeta", JOptionPane.OK_OPTION);
                    txtDPI.setText("");
                    lblNombre.setText("");
                    DefaultTableModel modelo = new DefaultTableModel();
                    String[] titulos = {"Tipo de Cuenta", "Numero de Cuenta"};
                    modelo.setColumnIdentifiers(titulos);
                    jTabla.setModel(modelo);
                    
                    
                    
                    
                }
            } catch (SQLException ex) {
                Logger.getLogger(TarjetaFrm.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
        
        
        

    }//GEN-LAST:event_btnSolicitarTarjetaActionPerformed

    
    
