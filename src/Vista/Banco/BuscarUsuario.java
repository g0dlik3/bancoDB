    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        UsuarioDB usuarioDB = new UsuarioDB();
        String nombre, apellido, telefono;
        char genero;
        nombre = txtNombre.getText();
        apellido = txtApellido.getText();
        dpi = txtDPI.getText();
        telefono = txtTelefono.getText();
        

        if (cBoxGenero.getSelectedIndex() == 0) {
            genero = 'H';
        } else {
            genero = 'M';
        }

        if (txtNombre.getText().isEmpty() || txtApellido.getText().isEmpty() || txtDPI.getText().isEmpty() || txtTelefono.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Porfavor rellene todos los campos", "Error", JOptionPane.ERROR_MESSAGE);

        } else {

            try {

                Usuario usuario = new Usuario(nombre, apellido, dpi, telefono, genero);
                if(usuarioDB.actualizarUsuario(usuario)== true){
                    JOptionPane.showMessageDialog(null, "Actualización realizada con exito!");
                    txtNombre.setText("");
                    txtApellido.setText("");
                    txtTelefono.setText("");
                    cBoxGenero.setEnabled(false);
                    btnActualizar.setEnabled(false);
                    txtDPI.setText("");
                    txtDPI.setEnabled(true);
                    txtDPI.setEditable(true);
                }else{
                    JOptionPane.showMessageDialog(null, "Ha ocurrido un error, intentelo nuevamente más tarde\n" + 
                                                        " si el problema persiste llame al 40137516", "Error", JOptionPane.ERROR);
                }

            } catch (SQLException ex) {
                Logger.getLogger(RegistroUsuario.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_btnActualizarActionPerformed

