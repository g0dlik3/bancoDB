    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        dpi = null;
        boolean existeUsuario = false;
        UsuarioDB usuarioDB = new UsuarioDB();
        if(!txtDPI.getText().isEmpty()){
            dpi = txtDPI.getText();
            try {
                existeUsuario = usuarioDB.existeUsuario(dpi);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Ha ocurrido un problema, intentelo mas tarde", "Error", JOptionPane.ERROR);
                ex.getMessage();
                ex.printStackTrace();
            }
                if(existeUsuario == true){
                    Usuario user = new Usuario();
                try {
                    btnActualizar.setEnabled(true);
                    user = usuarioDB.buscarUsuario(dpi);
                    txtNombre.setText(user.getNombre());
                    txtApellido.setText(user.getApellido());
                    txtTelefono.setText(user.getTelefono());
                    cBoxGenero.setEnabled(true);
                    txtDPI.setEditable(false);
                    txtDPI.setEnabled(false);
                    char genero = user.getGenero();
                    if(genero == 'H'){
                        cBoxGenero.setSelectedIndex(0);
                    }else{
                        cBoxGenero.setSelectedIndex(1);
                    }
                    
                    
                } catch (SQLException ex) {
                    Logger.getLogger(BuscarUsuario.class.getName()).log(Level.SEVERE, null, ex);
                }
                }else{
                    JOptionPane.showMessageDialog(null, "Este Usuario no existe", "Alerta!", JOptionPane.WARNING_MESSAGE);
                    txtNombre.setText("");
                    txtApellido.setText("");
                    txtTelefono.setText("");
                    cBoxGenero.setEnabled(false);
                    btnActualizar.setEnabled(false);
                }
            
            
        }else{
            JOptionPane.showMessageDialog(null, "Por favor ingrese el numero de DPI para poder continuar", "Alerta", JOptionPane.WARNING_MESSAGE);
            txtNombre.setText("");
                    txtApellido.setText("");
                    txtTelefono.setText("");
                    cBoxGenero.setEnabled(false);
                    btnActualizar.setEnabled(false);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

