    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        rus = new RegistroUsuarioSatisfactorio();
        int tipoCuenta = cBoxtipoCuenta.getSelectedIndex();
        try{    
        switch(tipoCuenta){
            
                case 0:
                    CuentaAhorro cuentaA = new CuentaAhorro(usuario);
                    usuarioDB.insertarCuentaA1(cuentaA);
                    rus.setInfoA(cuentaA);
                    this.dispose();
                    rus.setVisible(true);
                    
                    break;
                case 1:
                    CuentaMonetaria cuentaM = new CuentaMonetaria(usuario);
                    usuarioDB.insertarCuentaM1(cuentaM);  
                    rus.setInfoA(cuentaM);
                    this.dispose();
                    rus.setVisible(true);
            }
        }catch(SQLException e){
            e.printStackTrace();
            e.getMessage();
        }
        
        
        
        
        
    }//GEN-LAST:event_btnCrearActionPerformed

