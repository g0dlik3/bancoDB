    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        usuarioDB =  new UsuarioDB();
        
        if(txtDPI.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Rellene los campos porfavor", "Error", JOptionPane.ERROR_MESSAGE);
            btnCrear.setEnabled(false);
            labelNombre.setText("");
            labelApellido.setText("");
            usuario = null;
        }else{
            try {
                if(usuarioDB.existeUsuario(txtDPI.getText()) == true){

                    usuario = usuarioDB.buscarUsuario(txtDPI.getText());
                    labelNombre.setText(usuario.getNombre());
                    labelApellido.setText(usuario.getApellido());
                    btnCrear.setEnabled(true); 
                    
                }else{
                     btnCrear.setEnabled(false);
                     labelNombre.setText("");
                     labelApellido.setText("");
                     usuario = null;
                    JOptionPane.showMessageDialog(null, "Esta persona aun no se ha registrado", "Error", JOptionPane.ERROR_MESSAGE);
                }} catch (SQLException ex) {
                Logger.getLogger(RegistroCuenta.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

