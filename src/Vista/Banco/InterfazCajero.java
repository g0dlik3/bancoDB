    private void btnRetirar50ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRetirar50ActionPerformed
        UsuarioDB usuariodb =  new UsuarioDB();
        Transaccion transaccion = new Transaccion();
        Utilidades utilidad = new Utilidades();
        try {
            if(this.saldo >= 50){
            
                if(usuariodb.retirar(RETIRO_50, tarjeta.getCuenta().getNumeroCuenta()) == true){
                    TransaccionDB transdb= new TransaccionDB();
                    transaccion.setCuenta(tarjeta.getCuenta());
                    transaccion.setMonto(RETIRO_50);
                    transaccion.setFechaTransaccion(utilidad.setDateTime());
                    transaccion.setTipo_trans(RETIRO_CAJERO_AUTOMATICO);
                    transdb.insertTransaccion(transaccion);
                    JOptionPane.showMessageDialog(null, "Retiro exitoso");
                }else{
                    JOptionPane.showMessageDialog(null, "No se pudo hacer la transaccion");
                }
            
            }else{
                JOptionPane.showMessageDialog(null, "Saldo insuficiente");
            }
        } catch (SQLException ex) {
            Logger.getLogger(InterfazCajero.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_btnRetirar50ActionPerformed

