    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     //Mostrar ventana de opciones (boton enter de ventana login):
     String numeroTarjeta;
     int pin = Integer.parseInt(txtPin.getText());;
     boolean existeCuenta;
     TarjetaDB tarjetaDB = new TarjetaDB();
     
     numeroTarjeta = txtCuenta.getText();
     
     
        try {
            existeCuenta = tarjetaDB.existeTarjeta(numeroTarjeta);
            
            if(existeCuenta == true){
                if(tarjetaDB.loginCajero(numeroTarjeta, pin) == true){
                    
                    JOptionPane.showMessageDialog(null, "Logeo exitoso");
                    tarjeta = tarjetaDB.selectTarjeta(numeroTarjeta);
                    saldo = tarjeta.getCuenta().getSaldo();
                    System.out.println(saldo);
                    MostrarVentanaTipoCuenta(); 
                }else{
                    JOptionPane.showMessageDialog(null, "Numero de Tarjeta o PIN Incorrecto");
                }
                
                
            }else{
                JOptionPane.showMessageDialog(null, "No existe la tarjeta", "Error", JOptionPane.ERROR_MESSAGE);
            }
            
             
        } catch (SQLException ex) {
            Logger.getLogger(InterfazCajero.class.getName()).log(Level.SEVERE, null, ex);
        }
        
         
        
    }//GEN-LAST:event_jButton1ActionPerformed

